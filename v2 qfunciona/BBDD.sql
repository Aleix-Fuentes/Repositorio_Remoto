DROP DATABASE IF EXISTS juego;
CREATE DATABASE juego;
USE juego;

CREATE TABLE JUGADOR (
	USUARIO VARCHAR(20) NOT NULL,
	CONTRASENA VARCHAR(30),
	PARTIDASGANADAS INT,
	PARTIDASJUGADAS INT,
	PRIMARY KEY (USUARIO)
)ENGINE=InnoDB;

CREATE TABLE PARTIDAS (
	ID INTEGER NOT NULL ,
	USUARIO_G VARCHAR(20) ,
	DURACION INT,
	MEJORJUGADA INT,
	FECHA VARCHAR(20),
	HORA VARCHAR(10),
	FOREIGN KEY (USUARIO_G) REFERENCES JUGADOR(USUARIO),
	PRIMARY KEY (ID)
)ENGINE=InnoDB;

